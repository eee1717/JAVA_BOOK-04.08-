package ch02;

public class C03자료형 {

	public static void main(String[] args) {
		
		
		int num1; // 4byte 정수저장 공간을 지정한뒤 num1이름 부여(변수를 정의)
		num1 = 10; //10라는상수값을 상수풀에 저장한뒤 num1공간에 대입한다.
		
		int num2=4; // 4라는상수값을 상수풀에 저장한뒤 4byte 정수num2공간에 초기화 
		
		int num3 = num1 + num2 ; // num1값과 num2값의 합을 num3변수공간에 초기화
		
		System.out.println(num3); // num3의값을 출력
		
		
		// int num1 은 num1이라는 공간에 int의형태값을 넣겠다고 이야기하는것.
		// num1 =10 은 num1에 10의값을 넣은것. 
		//  int num2 =4는
		//  4라는값이있는공간이있고   num2라는빈공간에  4라는값을 넣는것이다..
        //  declaration:선언(이름부여)
        //  definition :정의 (이름부여+공간형성) 
        //  initialize :초기화(이름부여+공간형성+기본값저장)
        //		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
